# ── helpers ──────────────────────────────────────────────────────────────────

def card(slug, dest, audience, contributor, verified, hero,
         tldr, hoods, scams, transport, gems,
         checklist, dos, donts, budget, emergency,
         premium=False, premium_preview="", affiliates={}):
    return {
        "slug": slug, "destination": dest, "country": "India",
        "audience": audience, "contributorSlug": contributor,
        "lastUpdated": "2026-04-10", "verifiedByCount": verified,
        "heroImageUrl": hero,
        "tldr": tldr,
        "neighborhoods": hoods,
        "scams": scams,
        "transport": transport,
        "hiddenGems": gems,
        "preBookChecklist": checklist,
        "dosAndDonts": {"do": dos, "dont": donts},
        "estimatedDailyBudget": {**budget, "currency": "INR"},
        "emergencyNumbers": emergency,
        "isPremium": premium,
        "premiumPreview": premium_preview,
        "affiliateLinks": affiliates,
    }

def hood(name, rating, vibe, stay, notes):
    return {"name": name, "safetyRating": rating, "vibe": vibe,
            "stayHere": stay, "notes": notes}

def scm(title, severity, where, what, avoid):
    return {"title": title, "severity": severity, "where": where,
            "what": what, "avoid": avoid}

def trn(mode, tip, cost):
    return {"mode": mode, "tip": tip, "approxCost": cost}

def gem(name, type_, angle, why, cost):
    return {"name": name, "type": type_, "angle": angle,
            "why": why, "approxCost": cost}

def emg(label, number):
    return {"label": label, "number": number}

